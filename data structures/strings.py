
mystring = "HelloWorldFromMyFirstPython"

# get text in specific position of string

print(mystring[2])

# get subtext from speicic index till the end

print(mystring[5:])

# get subtext from begining till specific index not including this index

print(mystring[:4])

# get subtext from specific index to another specific index

print(mystring[21:])

# get all chars from the begining till the end

print(mystring[::])


# get all chars from the begining till the end by step of 2


print(mystring[::2])

# get chars from specific index to another by step of 2

print(mystring[2:2:2])

# reverse string, get all chars in revers

print(mystring[::-1])
